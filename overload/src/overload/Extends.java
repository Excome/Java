package overload;

public class Extends {
    private int test1;
    private int test2;
    Extends(int test1, int test2) {
        this.test1 = test1;
        this.test2 = test2;
    }
    public int getTest1() {
        return this.test1;
    }
    public int getTest2() {
        return this.test2;
    }
}
